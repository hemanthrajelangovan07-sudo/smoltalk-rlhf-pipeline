def fmt(x):
    m = re.search(r"####\s*([\-\d,\.]+)", x["answer"])
    num = m.group(1).replace(",", "") if m else None
    return {
        "prompt": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": x["question"]},
        ],
        "answer": num,
    }

train_data = raw.map(fmt).filter(lambda x: x["answer"] is not None)
