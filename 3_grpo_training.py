def _to_str(x):
    if isinstance(x, str):
        return x
    if isinstance(x, list):
        try:
            return tokenizer.decode(x, skip_special_tokens=True)
        except:
            return " ".join(str(s) for s in x)
    return str(x)

def extract_numeric_answer(text: str):
    text = _to_str(text)
    if not text:
        return None
    m = re.search(r"####\s*([\-\d,\.]+)", text)
    if m:
        return m.group(1).replace(",", "").strip()
    m = re.search(r"(?:answer\s+is|answer\s*:)\s*([\-\d,\.]+)", text, re.IGNORECASE)
    if m:
        return m.group(1).replace(",", "").strip()
    clean = text.replace(",", "")
    nums = re.findall(r"[\-]?\d+(?:\.\d+)?", clean)
    valid = [n for n in nums if _safe_float(n)]
    return valid[-1] if valid else None

def _safe_float(x):
    try: float(x); return True
    except: return False

def _nums_equal(p, t):
    try: return Decimal(p) == Decimal(t)
    except:
        try: return abs(float(p) - float(t)) < 1e-6
        except: return False

def has_reasoning_steps(text: str) -> bool:
    """Detect presence of step-by-step reasoning."""
    text = _to_str(text)
    patterns = [
        r"step\s*\d+", r"first", r"second", r"then", r"therefore",
        r"calculate", r"=\s*[\d]+", r"let\s+(me|us)", r"since", r"because",
    ]
    return sum(1 for p in patterns if re.search(p, text, re.IGNORECASE)) >= 2

def reward_correctness(prompts, completions, answer, **kwargs):
    """
    1.0 = correct answer
    0.5 = wrong answer but has valid reasoning structure
    0.0 = no answer or gibberish
    """
    rewards = []
    for c, a in zip(completions, answer):
        c_str = _to_str(c)
        if not c_str or a is None:
            rewards.append(0.0); continue
        p = extract_numeric_answer(c_str)
        if p and _nums_equal(p, str(a)):
            rewards.append(1.0)
        elif has_reasoning_steps(c_str):
            rewards.append(0.5)
        else:
            rewards.append(0.0)
    return rewards

def reward_format_quality(prompts, completions, **kwargs):
    """0.0-0.3: step-by-step format, numbers, final answer marker."""
    rewards = []
    for c in completions:
        c_str = _to_str(c)
        if not c_str:
            rewards.append(0.0); continue
        s = 0.0
        if len(re.findall(r"\d+", c_str)) >= 3: s += 0.1
        wc = len(c_str.split())
        if 30 <= wc <= 300: s += 0.1
        if re.search(r"(####|final answer|the answer is)", c_str, re.IGNORECASE): s += 0.1
        rewards.append(round(s, 4))
    return rewards

def reward_no_repetition(prompts, completions, **kwargs):
    """-0.5 to 0: penalize repetition via bigram diversity."""
    rewards = []
    for c in completions:
        c_str = _to_str(c)
        if not c_str or len(c_str.split()) < 2:
            rewards.append(0.0); continue
        w = c_str.lower().split()
        b = [f"{w[i]}_{w[i+1]}" for i in range(len(w)-1)]
        r = 1.0 - len(set(b))/len(b)
        rewards.append(round(-0.5 * min(r*2, 1.0), 4))
    return rewards

def reward_verbosity_penalty(prompts, completions, **kwargs):
    """Penalty for too-short (<20 words) or too-long (>500 words) completions."""
    rewards = []
    for c in completions:
        c_str = _to_str(c)
        if not c_str:
            rewards.append(-0.3); continue
        wc = len(c_str.split())
        if wc < 20:
            rewards.append(-0.2)
        elif wc > 500:
            rewards.append(-0.1)
        else:
            rewards.append(0.0)
    return rewards

