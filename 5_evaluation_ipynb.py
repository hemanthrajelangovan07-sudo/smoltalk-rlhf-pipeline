def _numbers_equal(pred: str, true: str) -> bool:
    if pred is None or true is None:
        return False
    try:
        return Decimal(pred) == Decimal(true)
    except InvalidOperation:
        try:
            return abs(float(pred) - float(true)) < 1e-6
        except (ValueError, TypeError):
            return False

def load_model_for_eval(adapter_repo=None):
    """Load model from Hub. No merge — PEFT refuses merge on 4-bit base."""
    from transformers import BitsAndBytesConfig
    bnb_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_compute_dtype=torch.float16,
    )
    tokenizer = AutoTokenizer.from_pretrained(TOKENIZER_ID)
    model = AutoModelForCausalLM.from_pretrained(
        BASE_MODEL_ID,
        quantization_config=bnb_config,
        device_map="auto",
    )
    if adapter_repo:
        model = PeftModel.from_pretrained(model, adapter_repo)
    model.eval()
    return model, tokenizer

def generate(model, tokenizer, prompt: str, max_new_tokens: int = 256) -> str:
    messages = [
        {"role": "system", "content": "You are a helpful and honest assistant."},
        {"role": "user",   "content": prompt},
    ]
    input_ids = tokenizer.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_tensors="pt",
    ).to(model.device)

    pad_id = tokenizer.pad_token_id if tokenizer.pad_token_id is not None \
        else tokenizer.eos_token_id
    attention_mask = (input_ids != pad_id).long()

    with torch.no_grad():
        outputs = model.generate(
            input_ids=input_ids,
            attention_mask=attention_mask,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id,
        )

    generated = outputs[0][input_ids.shape[1]:]
    return tokenizer.decode(generated, skip_special_tokens=True).strip()

def gsm8k_accuracy(model, tokenizer, n_samples=200) -> dict:
    from datasets import load_dataset
    dataset = load_dataset("openai/gsm8k", "main", split="test").select(range(n_samples))

    correct, results = 0, []
    for example in tqdm(dataset, desc="GSM8K", leave=False):
        true_match = re.search(r"####\s*([\-\d,\.]+)", example["answer"])
        true_ans = true_match.group(1).replace(",", "") if true_match else None

        pred_text = generate(model, tokenizer, example["question"], max_new_tokens=300)

        pred_match = re.search(r"####\s*([\-\d,\.]+)", pred_text)
        if not pred_match:
            pred_match = re.search(
                r"(?:answer\s+is|answer\s*:)\s*([\-\d,\.]+)",
                pred_text, re.IGNORECASE,
            )
        predicted = pred_match.group(1).replace(",", "") if pred_match else None

        is_correct = _numbers_equal(predicted, true_ans)
        correct += int(is_correct)
        results.append({
            "question":  example["question"][:100],
            "true":      true_ans,
            "predicted": predicted,
            "correct":   is_correct,
        })

    return {"accuracy": correct / n_samples, "correct": correct, "total": n_samples, "results": results}

def evaluate_win_rate_hf(
    model_a_outputs, model_b_outputs, prompts,
    model_a_name="A", model_b_name="B", hf_token=None,
) -> dict:
    from huggingface_hub import InferenceClient
    client = InferenceClient(model="Qwen/Qwen2.5-72B-Instruct", token=hf_token)

    JUDGE_PROMPT = """You are evaluating two AI assistant responses to a user query.

User query: {prompt}

Response A:
{response_a}

Response B:
{response_b}

Which response is better? Consider helpfulness, accuracy, and safety.
Answer with ONLY "A" or "B" or "TIE" (no explanation needed)."""

    a_wins = b_wins = ties = 0
    for prompt, out_a, out_b in tqdm(zip(prompts, model_a_outputs, model_b_outputs), total=len(prompts), desc="Win rate"):
        try:
            result = client.text_generation(
                JUDGE_PROMPT.format(prompt=prompt, response_a=out_a, response_b=out_b),
                max_new_tokens=5, temperature=0.0,
            ).strip().upper()
            if result.startswith("A"):
                a_wins += 1
            elif result.startswith("B"):
                b_wins += 1
            else:
                ties += 1
        except Exception:
            ties += 1

    total = len(prompts)
    return {
        f"{model_a_name}_wins": a_wins, f"{model_b_name}_wins": b_wins, "ties": ties,
        f"{model_a_name}_win_rate": a_wins / total,
        f"{model_b_name}_win_rate": b_wins / total,
        "tie_rate": ties / total,
    }

